package org.openelisglobal.organization.valueholder;

import org.openelisglobal.common.valueholder.BaseObject;

/**
 * Represents the association between an Organization and a TypeOfSample.
 * Indicates which sample types are supported by a given organization.
 */
public class OrganizationSampleType extends BaseObject<String> {

    private static final long serialVersionUID = 1L;

    private String id;
    private String organizationId;
    private String sampleTypeId;
    private Boolean isActive = true;
    private Integer sortOrder = 0;

    public OrganizationSampleType() {
        super();
    }

    @Override
    public String getId() {
        return id;
    }

    @Override
    public void setId(String id) {
        this.id = id;
    }

    public String getOrganizationId() {
        return organizationId;
    }

    public void setOrganizationId(String organizationId) {
        this.organizationId = organizationId;
    }

    public String getSampleTypeId() {
        return sampleTypeId;
    }

    public void setSampleTypeId(String sampleTypeId) {
        this.sampleTypeId = sampleTypeId;
    }

    public Boolean getIsActive() {
        return isActive;
    }

    public void setIsActive(Boolean isActive) {
        this.isActive = isActive;
    }

    public Integer getSortOrder() {
        return sortOrder;
    }

    public void setSortOrder(Integer sortOrder) {
        this.sortOrder = sortOrder;
    }
}
